# Define a string with the days of the week, separated by spaces.
days = "Mon Tue Wed Thu Fri Sat Sun"

# Define a string with the months of the year, separated by newline characters.
months = "Jan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"

# Print the days of the week.
print("Here are the days: ", days)

# Print the months of the year. Each month will be on a new line due to the newline characters.
print("Here are the months: ", months)

