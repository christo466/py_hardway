# Define a string variable with a tab character using the escape sequence '\t'.
tabby_cat = "\tI'm tabbed in."

# Define a string variable with a newline character using the escape sequence '\n'.
persian_cat = "I'm split\non a line."

# Define a string variable with backslashes using the escape sequence '\\'.
backlash_cat = "I'm \\ a \\ cat."

# Define a multiline string variable using triple quotes and including escape sequences.
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

# Print the contents of each string variable.
print(tabby_cat)    
print(persian_cat)  
print(backlash_cat) 
print(fat_cat)      